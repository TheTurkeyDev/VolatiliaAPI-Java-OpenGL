package main.java.VolatiliaOGL.util;

public class Dimension
{
	private float width;
	private float height;
	private float depth;
	
	public Dimension(float w, float h, float d)
	{
		width = w;
		height = h;
		depth = d;
	}
	
	public float getWidth()
	{
		return width;
	}
	public float getHeight()
	{
		return height;
	}
	public float getDepth()
	{
		return depth;
	}
}
